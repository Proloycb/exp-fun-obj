function addTwoNumbers (num1, num2) {
    var total = num1 + num2;
    return total;
}

var firstNumber = 21;
var secondNumber = 25;
var result = addTwoNumbers(firstNumber, secondNumber);
console.log (result);

function multiplyTwoNumbers (num1, num2) {
    var multiply = num1 * num2;
    return multiply;
}

var result = multiplyTwoNumbers (25, 22);
console.log (result);

function differenceTwoNumbers (num1, num2) {
    var difference = num1 - num2;
    return difference;
}

var result = differenceTwoNumbers (25, 22);
console.log (result);

function divisionTwoNumbers (num1, num2) {
    var division = num1 / num2;
    return division;
}

var result = divisionTwoNumbers (25, 22);
console.log (result);

